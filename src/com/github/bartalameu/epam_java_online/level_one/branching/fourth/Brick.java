package com.github.bartalameu.epam_java_online.level_one.branching.fourth;

public class Brick {
    private int x;
    private int y;
    private int z;

    Brick(int x, int y, int z) {

        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
